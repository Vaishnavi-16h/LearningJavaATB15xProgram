package For_Loop;

public class Lab017_While1
{
    public static void main(String[] args)
    {
        int i = 0; // Init
        while (i < 10) //condition
        {
            System.out.println(i);
            i++; // Updation
        }

        //  I, C, U
        for (int j = 0; j < 10 ; j++)
        {
            System.out.println(j);
        }

    }
}
